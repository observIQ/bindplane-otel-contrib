// Package enumcodegen generates Go enum data from JSON produced by scripts/dump_v21_enums.py.
package enumcodegen

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
)

// Run reads inPath (enums_v21.json), generates Go source, and writes to outPath (enums_generated.go).
func Run(inPath, outDir string) error {
	return RunForPackage(inPath, outDir, "validator",
		"Source: scripts/dump_v21_enums.py from cti-stix-validator v21/enums.py.")
}

// RunForPackage reads JSON from inPath, generates Go source for the given package and source comment, and writes to outDir/enums_generated.go.
func RunForPackage(inPath, outDir, pkgName, sourceComment string) error {
	data, err := os.ReadFile(inPath)
	if err != nil {
		return fmt.Errorf("read %s: %w", inPath, err)
	}
	var raw map[string]interface{}
	if err := json.Unmarshal(data, &raw); err != nil {
		return fmt.Errorf("parse JSON: %w", err)
	}
	code, err := GenerateWithPackage(raw, pkgName, sourceComment)
	if err != nil {
		return err
	}
	outPath := outDir
	if !strings.HasSuffix(outPath, ".go") {
		outPath = strings.TrimSuffix(outPath, "/") + "/enums_generated.go"
	}
	if err := os.MkdirAll(filepath.Dir(outPath), 0755); err != nil {
		return err
	}
	if err := os.WriteFile(outPath, code, 0644); err != nil {
		return fmt.Errorf("write %s: %w", outPath, err)
	}
	return nil
}

// emitter is a function that writes one enum variable to b. name and v are the JSON key and value.
type emitter func(b *strings.Builder, name string, v interface{}) error

// specialEmitters handle enum names that need custom formatting (not generic []string or map[string][]string).
var specialEmitters = map[string]emitter{
	"TIMESTAMP_COMPARE":                        emitTimestampCompare,
	"TIMESTAMP_COMPARE_OBSERVABLE":             emitTimestampCompare,
	"RELATIONSHIPS":                            emitRelationships,
	"OBSERVABLE_PROP_REFS":                     emitObservablePropRefs,
	"CHECK_CODES":                              emitCheckCodes,
	"TIMESTAMP_EMBEDDED_PROPERTIES":            emitMapMapStringSliceString,
	"OBSERVABLE_EMBEDDED_PROPERTIES":           emitMapMapStringSliceString,
	"OBSERVABLE_EXTENSION_EMBEDDED_PROPERTIES": emitMapMapStringSliceString,
}

// Generate produces Go source for package validator from the parsed JSON map.
// Deprecated: use GenerateWithPackage for new callers.
func Generate(raw map[string]interface{}) ([]byte, error) {
	return GenerateWithPackage(raw, "validator",
		"Source: scripts/dump_v21_enums.py from cti-stix-validator v21/enums.py.")
}

// GenerateWithPackage produces Go source for the given package from the parsed JSON map.
// sourceComment is written in the header (e.g. "Source: scripts/dump_matcher_enums.py from cti-pattern-matcher stix2matcher/enums.py.").
func GenerateWithPackage(raw map[string]interface{}, pkgName, sourceComment string) ([]byte, error) {
	var b strings.Builder
	b.WriteString("// Code generated by github.com/observiq/stix-go/cmd/codegen-enums. DO NOT EDIT.\n")
	b.WriteString("// ")
	b.WriteString(sourceComment)
	b.WriteString("\n\n")
	b.WriteString("package ")
	b.WriteString(pkgName)
	b.WriteString("\n\n")

	for _, name := range sortedKeys(raw) {
		v := raw[name]
		if v == nil {
			continue
		}
		if emit, ok := specialEmitters[name]; ok {
			if err := emit(&b, name, v); err != nil {
				return nil, err
			}
		} else if err := emitGeneric(&b, name, v); err != nil {
			return nil, err
		}
		b.WriteString("\n")
	}
	return []byte(b.String()), nil
}

// --- Helpers: map/slice handling ---

func sortedKeys(m map[string]interface{}) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

// asStringSlice converts []interface{} to []string; returns error if any element is not a string.
func asStringSlice(arr []interface{}, ctx string) ([]string, error) {
	out := make([]string, 0, len(arr))
	for i, e := range arr {
		s, ok := e.(string)
		if !ok {
			return nil, fmt.Errorf("%s: element %d not string (got %T)", ctx, i, e)
		}
		out = append(out, s)
	}
	return out, nil
}

// asMap asserts v as map[string]interface{}.
func asMap(v interface{}, name string) (map[string]interface{}, error) {
	m, ok := v.(map[string]interface{})
	if !ok {
		return nil, fmt.Errorf("%s: expected map, got %T", name, v)
	}
	return m, nil
}

// --- Emitters (all write a single "var Name = ..." block) ---

func emitGeneric(b *strings.Builder, name string, v interface{}) error {
	switch x := v.(type) {
	case []interface{}:
		strs, err := asStringSlice(x, name)
		if err != nil {
			return err
		}
		emitStringSlice(b, name, strs)
		return nil
	case map[string]interface{}:
		if allValuesAreStringSlices(x) {
			return emitMapStringSliceString(b, name, x)
		}
		return emitMapStringInterface(b, name, x)
	default:
		return fmt.Errorf("%s: unsupported type %T", name, v)
	}
}

func allValuesAreStringSlices(m map[string]interface{}) bool {
	for _, v := range m {
		if _, ok := v.([]interface{}); !ok {
			return false
		}
	}
	return true
}

func emitStringSlice(b *strings.Builder, name string, s []string) {
	b.WriteString("var ")
	b.WriteString(name)
	b.WriteString(" = []string{\n")
	for _, v := range s {
		b.WriteString("\t")
		b.WriteString(strconv.Quote(v))
		b.WriteString(",\n")
	}
	b.WriteString("}\n")
}

func emitSliceLiteral(b *strings.Builder, s []string) {
	b.WriteString("[]string{")
	for i, v := range s {
		if i > 0 {
			b.WriteString(", ")
		}
		b.WriteString(strconv.Quote(v))
	}
	b.WriteString("}")
}

func emitMapStringSliceString(b *strings.Builder, name string, m map[string]interface{}) error {
	b.WriteString("var ")
	b.WriteString(name)
	b.WriteString(" = map[string][]string{\n")
	for _, k := range sortedKeys(m) {
		arr, ok := m[k].([]interface{})
		if !ok {
			return fmt.Errorf("%s[%q]: value not array", name, k)
		}
		strs, err := asStringSlice(arr, name+"["+strconv.Quote(k)+"]")
		if err != nil {
			return err
		}
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": ")
		emitSliceLiteral(b, strs)
		b.WriteString(",\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitCheckCodes(b *strings.Builder, name string, v interface{}) error {
	m, err := asMap(v, name)
	if err != nil {
		return err
	}
	b.WriteString("var CHECK_CODES = map[string]string{\n")
	for _, k := range sortedKeys(m) {
		val, _ := m[k].(string)
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": ")
		b.WriteString(strconv.Quote(val))
		b.WriteString(",\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitMapMapStringSliceString(b *strings.Builder, name string, v interface{}) error {
	m, err := asMap(v, name)
	if err != nil {
		return err
	}
	b.WriteString("var ")
	b.WriteString(name)
	b.WriteString(" = map[string]map[string][]string{\n")
	for _, k := range sortedKeys(m) {
		inner, err := asMap(m[k], name+"["+strconv.Quote(k)+"]")
		if err != nil {
			return err
		}
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": map[string][]string{\n")
		for _, ik := range sortedKeys(inner) {
			arr, ok := inner[ik].([]interface{})
			if !ok {
				return fmt.Errorf("%s[%q][%q]: not array", name, k, ik)
			}
			strs, err := asStringSlice(arr, name+"["+strconv.Quote(k)+"]["+strconv.Quote(ik)+"]")
			if err != nil {
				return err
			}
			b.WriteString("\t\t")
			b.WriteString(strconv.Quote(ik))
			b.WriteString(": ")
			emitSliceLiteral(b, strs)
			b.WriteString(",\n")
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitTimestampCompare(b *strings.Builder, name string, v interface{}) error {
	m, err := asMap(v, name)
	if err != nil {
		return err
	}
	b.WriteString("var ")
	b.WriteString(name)
	b.WriteString(" = map[string][][3]string{\n")
	for _, k := range sortedKeys(m) {
		arr, ok := m[k].([]interface{})
		if !ok {
			return fmt.Errorf("%s[%q]: not array", name, k)
		}
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": [][3]string{\n")
		for _, item := range arr {
			tup, ok := item.([]interface{})
			if !ok || len(tup) != 3 {
				return fmt.Errorf("%s[%q]: tuple not [3]", name, k)
			}
			first, _ := tup[0].(string)
			op, _ := tup[1].(string)
			second, _ := tup[2].(string)
			b.WriteString("\t\t{")
			b.WriteString(strconv.Quote(first))
			b.WriteString(", ")
			b.WriteString(strconv.Quote(op))
			b.WriteString(", ")
			b.WriteString(strconv.Quote(second))
			b.WriteString("},\n")
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitRelationships(b *strings.Builder, _ string, v interface{}) error {
	m, err := asMap(v, "RELATIONSHIPS")
	if err != nil {
		return err
	}
	b.WriteString("var RELATIONSHIPS = map[string]map[string][]string{\n")
	for _, src := range sortedKeys(m) {
		inner, err := asMap(m[src], "RELATIONSHIPS["+strconv.Quote(src)+"]")
		if err != nil {
			return err
		}
		b.WriteString("\t")
		b.WriteString(strconv.Quote(src))
		b.WriteString(": map[string][]string{\n")
		for _, relType := range sortedKeys(inner) {
			targets := relationshipTargets(inner[relType])
			b.WriteString("\t\t")
			b.WriteString(strconv.Quote(relType))
			b.WriteString(": ")
			emitSliceLiteral(b, targets)
			b.WriteString(",\n")
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n")
	return nil
}

// relationshipTargets normalizes Python value (string or []string) to []string.
func relationshipTargets(v interface{}) []string {
	switch x := v.(type) {
	case string:
		return []string{x}
	case []interface{}:
		out := make([]string, 0, len(x))
		for _, e := range x {
			if s, ok := e.(string); ok {
				out = append(out, s)
			}
		}
		return out
	}
	return nil
}

func emitObservablePropRefs(b *strings.Builder, _ string, v interface{}) error {
	m, err := asMap(v, "OBSERVABLE_PROP_REFS")
	if err != nil {
		return err
	}
	b.WriteString("var OBSERVABLE_PROP_REFS = map[string]interface{}{\n")
	for _, k := range sortedKeys(m) {
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": ")
		if err := emitObsPropRefValue(b, m[k]); err != nil {
			return err
		}
		b.WriteString(",\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitObsPropRefValue(b *strings.Builder, v interface{}) error {
	switch x := v.(type) {
	case []interface{}:
		strs := make([]string, 0, len(x))
		for _, e := range x {
			if s, ok := e.(string); ok {
				strs = append(strs, s)
			}
		}
		emitSliceLiteral(b, strs)
		return nil
	case map[string]interface{}:
		b.WriteString("map[string]interface{}{\n")
		for _, k := range sortedKeys(x) {
			b.WriteString("\t\t")
			b.WriteString(strconv.Quote(k))
			b.WriteString(": ")
			if err := emitObsPropRefValue(b, x[k]); err != nil {
				return err
			}
			b.WriteString(",\n")
		}
		b.WriteString("\t}")
		return nil
	default:
		return fmt.Errorf("OBSERVABLE_PROP_REFS value: unexpected %T", v)
	}
}

func emitMapStringInterface(b *strings.Builder, name string, m map[string]interface{}) error {
	b.WriteString("var ")
	b.WriteString(name)
	b.WriteString(" = map[string]interface{}{\n")
	for _, k := range sortedKeys(m) {
		b.WriteString("\t")
		b.WriteString(strconv.Quote(k))
		b.WriteString(": ")
		if err := emitInterfaceLiteral(b, m[k]); err != nil {
			return fmt.Errorf("%s: %w", name, err)
		}
		b.WriteString(",\n")
	}
	b.WriteString("}\n")
	return nil
}

func emitInterfaceLiteral(b *strings.Builder, v interface{}) error {
	switch x := v.(type) {
	case string:
		b.WriteString(strconv.Quote(x))
		return nil
	case []interface{}:
		if strs, err := asStringSlice(x, ""); err == nil {
			emitSliceLiteral(b, strs)
			return nil
		}
		b.WriteString("[]interface{}{")
		for i, e := range x {
			if i > 0 {
				b.WriteString(", ")
			}
			if err := emitInterfaceLiteral(b, e); err != nil {
				return err
			}
		}
		b.WriteString("}")
		return nil
	case map[string]interface{}:
		b.WriteString("map[string]interface{}{\n")
		for _, k := range sortedKeys(x) {
			b.WriteString("\t")
			b.WriteString(strconv.Quote(k))
			b.WriteString(": ")
			if err := emitInterfaceLiteral(b, x[k]); err != nil {
				return err
			}
			b.WriteString(",\n")
		}
		b.WriteString("}")
		return nil
	default:
		return fmt.Errorf("unsupported type %T", v)
	}
}
