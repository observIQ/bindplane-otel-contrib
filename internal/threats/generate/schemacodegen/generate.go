package codegen

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// refs maps schema path (e.g. "common/identifier.json") to Go type name.
func defaultRefs() map[string]string {
	return map[string]string{
		"common/identifier.json":  "string",
		"common/timestamp.json":   "string",
		"common/url-regex.json":   "string",
		"common/hex.json":         "string",
		"common/binary.json":      "[]byte",
		"common/hashes-type.json": "map[string]string",
		"common/dictionary.json":  "map[string]interface{}",
		"common/properties.json":  "map[string]interface{}", // patternProperties
	}
}

// RootObjectPaths returns schema paths that should get a generated struct (SDOs, SROs, observables, key common).
func RootObjectPaths(schemas map[string]Schema) []string {
	roots := []string{
		"common/bundle.json",
		"common/core.json",
		"common/cyber-observable-core.json",
		"common/marking-definition.json",
		"common/language-content.json",
		"common/external-reference.json",
		"common/granular-marking.json",
		"common/extension.json",
		"common/extension-definition.json",
		"common/kill-chain-phase.json",
		"common/observed-data-refs.json", // may not exist
	}
	// Add all sdos, sros, observables
	for path := range schemas {
		if strings.HasPrefix(path, "sdos/") || strings.HasPrefix(path, "sros/") || strings.HasPrefix(path, "observables/") {
			s := schemas[path]
			if GetType(s) == "object" || len(GetArray(s, "allOf")) > 0 {
				roots = append(roots, path)
			}
		}
	}
	seen := make(map[string]bool)
	var out []string
	for _, r := range roots {
		if schemas[r] != nil && !seen[r] {
			seen[r] = true
			out = append(out, r)
		}
	}
	return out
}

// Generate produces Go source code for the stixx/models package.
func Generate(schemas map[string]Schema, refs map[string]string) ([]byte, error) {
	if refs == nil {
		refs = defaultRefs()
	}
	for k, v := range defaultRefs() {
		if refs[k] == "" {
			refs[k] = v
		}
	}

	var buf strings.Builder
	buf.WriteString("// Code generated by github.com/observiq/stix-go/cmd/codegen. DO NOT EDIT.\n")
	buf.WriteString("// Source: STIX 2.1 JSON schemas (cti-stix2-json-schemas).\n\n")
	buf.WriteString("package stix\n\n")

	roots := RootObjectPaths(schemas)
	// Emit structs for object types that are referenced (e.g. external-reference, granular-marking) first
	// so they're defined before use. Simple order: common first, then sdos, sros, observables.
	order := make([]string, 0, len(roots))
	for _, path := range roots {
		if path == "common/properties.json" || path == "common/dictionary.json" || path == "common/observed-data-refs.json" {
			continue
		}
		s := schemas[path]
		if s == nil {
			continue
		}
		title := GetString(s, "title")
		if title == "" {
			title = strings.TrimSuffix(filepath.Base(path), ".json")
		}
		typeName := goTypeName(title)
		props, required := FlattenObjectSchema(schemas, path)
		if props == nil && GetType(s) != "object" && len(GetArray(s, "allOf")) == 0 {
			continue
		}
		if props == nil {
			props = make(map[string]Schema)
		}
		order = append(order, path)
		_ = typeName
		_ = required
	}

	emitted := make(map[string]bool)
	// Emit in dependency order: common types first, then SDOs/SROs/observables
	for _, path := range []string{
		"common/external-reference.json",
		"common/granular-marking.json",
		"common/kill-chain-phase.json",
		"common/extension.json",
		"common/marking-definition.json",
		"common/language-content.json",
		"common/extension-definition.json",
		"common/core.json",
		"common/cyber-observable-core.json",
		"common/bundle.json",
	} {
		if emitted[path] || schemas[path] == nil {
			continue
		}
		s := schemas[path]
		title := GetString(s, "title")
		if title == "" {
			title = strings.TrimSuffix(filepath.Base(path), ".json")
		}
		typeName := goTypeName(title)
		props, required := FlattenObjectSchema(schemas, path)
		if props != nil {
			EmitStruct(&buf, typeName, props, required, path, schemas, refs)
			emitted[path] = true
		}
	}
	for _, path := range order {
		if emitted[path] {
			continue
		}
		s := schemas[path]
		title := GetString(s, "title")
		if title == "" {
			title = strings.TrimSuffix(filepath.Base(path), ".json")
		}
		typeName := goTypeName(title)
		props, required := FlattenObjectSchema(schemas, path)
		if props != nil {
			EmitStruct(&buf, typeName, props, required, path, schemas, refs)
			emitted[path] = true
		}
	}

	// Bundle.Objects is array of mixed types - use interface{} or a wrapper
	out := []byte(buf.String())
	return out, nil
}

// Run loads schemas from inDir, generates Go, and writes to outDir (package directory).
func Run(inDir, outDir string) error {
	schemas, err := LoadSchemaDir(inDir)
	if err != nil {
		return fmt.Errorf("load schemas: %w", err)
	}
	refs := defaultRefs()
	// Register refs for common object types so they become pointer types in structs
	refs["common/external-reference.json"] = "*ExternalReference"
	refs["common/granular-marking.json"] = "*GranularMarking"
	refs["common/kill-chain-phase.json"] = "*KillChainPhase"
	refs["common/extension.json"] = "map[string]interface{}"
	refs["common/marking-definition.json"] = "*MarkingDefinition"
	refs["common/language-content.json"] = "*LanguageContent"
	refs["common/extension-definition.json"] = "*ExtensionDefinition"

	code, err := Generate(schemas, refs)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(outDir, 0755); err != nil {
		return err
	}
	outFile := filepath.Join(outDir, "models.go")
	return os.WriteFile(outFile, code, 0644)
}
