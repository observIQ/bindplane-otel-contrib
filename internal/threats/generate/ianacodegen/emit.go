package ianacodegen

import (
	"fmt"
	"sort"
	"strings"
)

// Generate produces the Go source for package stix (stix/iana.go).
func Generate(d *Data) ([]byte, error) {
	var b strings.Builder
	b.WriteString("// Code generated by github.com/observiq/stix-go/cmd/codegen-iana. DO NOT EDIT.\n")
	b.WriteString("// Source: cti-stix-validator/stix2validator/v21/assets/*.csv\n")
	b.WriteString("// (mediatype_*.csv, charsets.csv, protocols.csv, ipfix-information-elements.csv).\n\n")
	b.WriteString("package stix\n\n")

	// MIME types
	b.WriteString("var validMIMETypes = map[string]struct{}{\n")
	for _, s := range d.MediaTypes {
		b.WriteString(fmt.Sprintf("%q: {},\n", s))
	}
	b.WriteString("}\n\n")

	// Charsets
	b.WriteString("var validCharsets = map[string]struct{}{\n")
	for _, s := range d.Charsets {
		b.WriteString(fmt.Sprintf("%q: {},\n", s))
	}
	b.WriteString("}\n\n")

	// Protocols
	b.WriteString("var validProtocols = map[string]struct{}{\n")
	for _, s := range d.Protocols {
		b.WriteString(fmt.Sprintf("%q: {},\n", s))
	}
	b.WriteString("}\n\n")

	// IPFIX names
	b.WriteString("var validIPFIXNames = map[string]struct{}{\n")
	for _, s := range d.IPFIXNames {
		b.WriteString(fmt.Sprintf("%q: {},\n", s))
	}
	b.WriteString("}\n\n")

	b.WriteString("// IsValidMIMEType reports whether s is a valid IANA media (MIME) type.\n")
	b.WriteString("func IsValidMIMEType(s string) bool { _, ok := validMIMETypes[s]; return ok }\n\n")
	b.WriteString("// IsValidCharset reports whether s is a valid IANA character set name.\n")
	b.WriteString("func IsValidCharset(s string) bool { _, ok := validCharsets[s]; return ok }\n\n")
	b.WriteString("// IsValidProtocol reports whether s is a valid protocol (IANA service/transport + ipv4/ipv6/ssl/tls/dns).\n")
	b.WriteString("func IsValidProtocol(s string) bool { _, ok := validProtocols[s]; return ok }\n\n")
	b.WriteString("// IsValidIPFIXName reports whether s is a valid IANA IPFIX Information Element name.\n")
	b.WriteString("func IsValidIPFIXName(s string) bool { _, ok := validIPFIXNames[s]; return ok }\n")

	return []byte(b.String()), nil
}

// KnownValues returns a minimal set of known values for golden tests (one per set).
func KnownValues(d *Data) (mime, charset, protocol, ipfix string) {
	if len(d.MediaTypes) > 0 {
		idx := sort.SearchStrings(d.MediaTypes, "application/zip")
		if idx < len(d.MediaTypes) && d.MediaTypes[idx] == "application/zip" {
			mime = "application/zip"
		} else {
			mime = d.MediaTypes[0]
		}
	}
	if len(d.Charsets) > 0 {
		idx := sort.SearchStrings(d.Charsets, "UTF-8")
		if idx < len(d.Charsets) && d.Charsets[idx] == "UTF-8" {
			charset = "UTF-8"
		} else {
			charset = d.Charsets[0]
		}
	}
	if len(d.Protocols) > 0 {
		idx := sort.SearchStrings(d.Protocols, "tcp")
		if idx < len(d.Protocols) && d.Protocols[idx] == "tcp" {
			protocol = "tcp"
		} else {
			protocol = d.Protocols[0]
		}
	}
	if len(d.IPFIXNames) > 0 {
		idx := sort.SearchStrings(d.IPFIXNames, "octetDeltaCount")
		if idx < len(d.IPFIXNames) && d.IPFIXNames[idx] == "octetDeltaCount" {
			ipfix = "octetDeltaCount"
		} else {
			ipfix = d.IPFIXNames[0]
		}
	}
	return mime, charset, protocol, ipfix
}
